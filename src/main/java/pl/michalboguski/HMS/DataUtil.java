    public String capitalize(String word){
        return word.substring(0,1).toUpperCase() + word.substring(1).toLowerCase();
    }

    public List<EmployeeDTO> create10Emplyees(){
        List<EmployeeDTO> new10EmployeeDTO = new ArrayList<>();
        System.out.println("ADD 10 ACTIVATION");
        Random random = new Random();
        for (int i = 0; i < 10; i++) {
            String name;
            String lastName;
            if (i % 2 == 0 ) {
                name = getMenNames()[random.nextInt(menNames.length)];
                lastName = getMenLastNames()[random.nextInt(menLastNames.length)];
            } else {
                name = getWomenNames()[random.nextInt(womenNames.length)];
                lastName = getWomenLastNames()[random.nextInt(womenLastNames.length)];
            }
            EmployeeDTO newEMployee = new EmployeeDTO();
            newEMployee.setName(capitalize(name));
            newEMployee.setSurname(capitalize(lastName));
            newEMployee.setDob(LocalDate.of(1955+random.nextInt(50),random.nextInt(12)+1,random.nextInt(28)+1));
            new10EmployeeDTO.add(newEMployee);
        }
        return new10EmployeeDTO;
    }


